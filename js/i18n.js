window.getInitialLanguage = function() {
    var saved = null;
    try {
        saved = window.localStorage.getItem('tkclc_pro_lang');
    } catch (_error) {
        saved = window.__tkclc_lang || null;
    }
    return translations[saved] ? saved : 'zh-TW';
};

window.setLanguage = function(lang) {
    if (translations[lang]) {
        try {
            window.localStorage.setItem('tkclc_pro_lang', lang);
        } catch (_error) {
            window.__tkclc_lang = lang;
        }
    }
};

window.t = function(key) {
    var lang = 'zh-TW';
    try {
        lang = window.localStorage.getItem('tkclc_pro_lang') || window.__tkclc_lang || 'zh-TW';
    } catch (_error) {
        lang = window.__tkclc_lang || 'zh-TW';
    }

    if (translations[lang] && typeof translations[lang][key] !== 'undefined') {
        return translations[lang][key];
    }

    if (typeof translations['zh-TW'][key] !== 'undefined') {
        return translations['zh-TW'][key];
    }

    return key;
};
